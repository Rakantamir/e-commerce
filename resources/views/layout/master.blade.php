        // Event listener untuk tombol "Buy Now"
        const buyNowButtons = document.querySelectorAll(".buy-now-button");
        buyNowButtons.forEach(button => {
            button.addEventListener("click", function() {
                const productName = this.getAttribute("data-product");
                showProductDetails(productName);
            });
        });

        // Event listener untuk tombol "See More"
        const seeMoreButtons = document.querySelectorAll(".see-more-button");
        seeMoreButtons.forEach(button => {
            button.addEventListener("click", function() {
                const productName = this.getAttribute("data-product");
                showProductDetails(productName);
            });
        });

        // Fungsi untuk menampilkan detail produk dalam modal
        function showProductDetails(productName) {
            const modal = document.getElementById("productModal");
            const modalProductName = modal.querySelector(".modal-product-name");
            const modalProductPrice = modal.querySelector(".modal-product-price");
            const modalProductOptions = modal.querySelector(".modal-product-options");
            const modalProductDescription = modal.querySelector(".modal-product-description");

            // Dapatkan harga berdasarkan ukuran yang dipilih
            const selectedSize = document.getElementById("product-size").value;
            const selectedColor = document.getElementById("product-color").value;
            const priceAttribute = "data-price-" + selectedSize.toLowerCase();
            const productPrice = document.querySelector(".product-price").getAttribute(priceAttribute);

            modalProductName.textContent = productName;
            modalProductPrice.textContent = "Price: $" + productPrice;
            modalProductOptions.style.display = "block";
            modalProductDescription.style.display = "none";

            const addToCartButton = document.getElementById("addToCartButton");
            addToCartButton.addEventListener("click", function() {
                // Lakukan sesuatu dengan data yang dipilih, seperti menambahkannya ke keranjang belanja
